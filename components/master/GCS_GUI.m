function ManAuto(hObjects, id)
global Robots

val = get(hObject, 'Value');
if val == 1
    %in Manual
    set(hObject, 'String', 'Manual');
    Robots(id).manauto = 'man';
    set(hObject, 'BackgroundColor', 'red');
else
    set(hObject, 'String', 'Auto');
    Robots(id).manauto = 'auto';
    set(hObject, 'BackgroundColor', 'green');
end


function connectIPC(ids)
masterConnectRobots(ids);
messages = {'PoseExternal', ...
            'IncMapUpdateH', ...
            'IncMapUpdateV'};
handles  = {@handlerPoseExternal, ...
            @handlerIncMapUpdateH, ...
            @handlerIncMapUpdateV};
         
queueLengths = [5 5 5];
%subscribe to messages
masterSubscribeRobots(messages, handles, queueLengths);



