function [best,best_mask,bound] = find_center(img)
	imgs = imresize(img,1/5);   
	outer_mask = ~circle(120,[size(imgs,1),size(imgs,2)]);
	rgb = stat_mask(imgs,outer_mask);
	%score = 0.3*(img(:,:,1) - rgb(1)) + 0.59* (img(:,:,2) - rgb(2)) + .11 * (img(:,:,3) - rgb(3));  
	score = double(imgs(:,:,1) - rgb(1)).^2 + double(imgs(:,:,2) - rgb(2)).^2 + double(imgs(:,:,3) - rgb(3)).^2;  
	score_outer = score(outer_mask);
	sorted = sort(score_outer); 
	max_score = sorted(round(numel(sorted)*.70)); 
	search = score < max_score; 
	%search = imresize(double(search), 1/5, 'nearest');
	search = bwareaopen(search,4000);    
	search = ~bwareaopen(~search,4000);    
	bound = search;
	bound = bwperim(search);
	bound(1,:) = 0;  
	bound(end,:) = 0;  
	bound(:,1) = 0;  
	bound(:,end) = 0;  
	best = circle_hough(bound,120,130);
	best_mask = circle(best(3),size(search),best(1:2));
	best = best * 5;   
